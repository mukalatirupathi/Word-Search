package DAO;

public class WordSearchEntity {
    private String inputFilePath;
    private String inputWord;
    private String search_time;
    private String resultMsg;
    private int matchingWordCount;
    private String errorMessage;

    public String getInputFilePath() {
        return inputFilePath;
    }

    public void setInputFilePath(String inputFilePath) {
        this.inputFilePath = inputFilePath;
    }

    public String getInputWord() {
        return inputWord;
    }

    public void setInputWord(String inputWord) {
        this.inputWord = inputWord;
    }

    public String getSearch_time() {
        return search_time;
    }

    public void setSearch_time(String search_time) {
        this.search_time = search_time;
    }

    public String getResultMsg() {
        return resultMsg;
    }

    public void setResultMsg(String resultMsg) {
        this.resultMsg = resultMsg;
    }

    public int getMatchingWordCount() {
        return matchingWordCount;
    }

    public void setMatchingWordCount(int matchingWordCount) {
        this.matchingWordCount = matchingWordCount;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }
}
